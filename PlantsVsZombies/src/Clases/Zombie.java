package Clases;

import javax.swing.JLabel;

/**
 *
 * @author Adrian
 */
public class Zombie extends JLabel implements Runnable {
    private String imagen;
    private String nombre;
    private int ataque;
    private int vida;
    private String tipo;
    private int x = 0;
    private int y = 0;

    public Zombie(String imagen, String nombre, int ataque, int vida, String tipo) {
        this.imagen = imagen;
        this.nombre = nombre;
        this.ataque = ataque;
        this.vida = vida;
        this.tipo = tipo;
    }

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getAtaque() {
        return ataque;
    }

    public void setAtaque(int ataque) {
        this.ataque = ataque;
    }

    public int getVida() {
        return vida;
    }

    public void setVida(int vida) {
        this.vida = vida;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }     

    @Override
    public void run() {
        
    }
}
